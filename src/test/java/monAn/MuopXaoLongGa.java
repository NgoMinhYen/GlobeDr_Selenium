package monAn;

public class MuopXaoLongGa extends MonXao{
    @Override
    void chuanBiNguyenLieu() {
        System.out.println("Mua mướp và lòng gà tươi ở chợ hoặc siêu thị bạn nhé");
    }

    @Override
    void soCheNguyLieu() {
        System.out.println("Rửa sạch lòng gà ướp với tí tiêu, muối, bọt ngot và tỏi" +
                ", gọt vỏ mướp, rửa sạch và cắt miếng vừa ăn");
    }

    @Override
    void xao() {
        System.out.println("Đặt chảo lên bếp và cho thêm tí dầu, đợt dầu nóng bỏ tỏi vào cho dậy mùi," +
                "bỏ lòng gà vào xào chín, bỏ mướp vào và nêm nếm gia vị cho vừa ăn");
    }

    @Override
    void trangTri() {
        System.out.println("Bỏ ra dĩa và ăn thôi nào Mập ơi");
    }
}
