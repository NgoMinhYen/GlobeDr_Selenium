package monAn;

public class RauMuongXaoToi extends MonXao{
    @Override
    void chuanBiNguyenLieu() {
        System.out.println("Mua rau muống và tỏi ở chợ hoặc siêu thị bạn nhé");
    }

    @Override
    void soCheNguyLieu() {
        System.out.println("Lặt rau muốn bỏ nhưng phần già, lột vỏ tỏi và đập dập," +
                " chuẩn bị một cái chảo");

    }

    @Override
    void xao() {
        System.out.println("Để chảo trên lửa nóng và đổ dầu vào, khi dầu nóng bỏ tỏi dã đập dập vào" +
                "xào cho dậy mùi, tiếp theo bỏ rau muốn vào và hteem gia vị cho vừa ăn nhé");
    }

    @Override
    void trangTri() {
        System.out.println("Rau muốn xào xong bỏ ra dĩa và thưởng thức cũng cơm trắng thôi nào");
    }
}
